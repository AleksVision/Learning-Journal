""" 
Задача 2
В большой текстовой строке подсчитать количество встречаемых слов и вернуть 10 самых частых. Не учитывать знаки препинания и регистр символов. За основу возьмите любую статью из википедии или из документации к языку.
"""
import collections
import string

# Исходный текст (в качестве примера возьмем часть текста статьи из Википедии)
text = """
Python - это высокоуровневый язык программирования общего назначения, который используется для решения самых разнообразных задач. Он был создан Гвидо ван Россумом и впервые выпущен в 1991 году. Python позволяет писать простой и читабельный код, поддерживает объектно-ориентированное программирование и имеет богатую стандартную библиотеку.
"""

# Приводим текст к нижнему регистру и удаляем знаки препинания
text = text.lower().translate(str.maketrans("", "", string.punctuation))

# Разбиваем текст на слова
words = text.split()

# Подсчитываем количество вхождений каждого слова
word_counts = collections.Counter(words)

# Получаем 10 самых частых слов
most_common_words = word_counts.most_common(10)

# Выводим результат
print("10 самых частых слов и их количество встречаемости:")
for word, count in most_common_words:
    print(f"{word}: {count}")

