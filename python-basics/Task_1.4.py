""" 
Нарисовать в консоли ёлочку, спросив 
у пользователя количество ярусов и высоту яруса.
Пример результата:
Сколько ярусов у ёлочки? 5
Какова высота каждого яруса? 3

        *
       ***
      *****
     *******
    *********
"""

# Вариант 1: Елочка с ярусами и высотой яруса
# Запрашиваем у пользователя количество ярусов и высоту яруса
tiers = int(input("Сколько ярусов у ёлочки? "))  # Количество ярусов
height_per_tier = int(input("Какова высота каждого яруса? "))  # Высота каждого яруса

# Строим ёлочку
for tier in range(tiers):  # Перебираем ярусы
    for row in range(height_per_tier):  # Перебираем строки в ярусе
        num_stars = 2 * (tier * height_per_tier + row) + 1  # Вычисляем количество звездочек
        num_spaces = tiers * height_per_tier - (tier * height_per_tier + row + 1)  # Вычисляем количество пробелов
        print(" " * num_spaces + "*" * num_stars)  # Выводим строку

# Вариант 2: Простая елочка с фиксированным количеством рядов
rows = int(input("Сколько рядов у ёлочки? "))  # Количество рядов
for i in range(1, rows + 1):  # Перебираем ряды
    spaces = rows - i  # Вычисляем количество пробелов
    stars = 2 * i - 1  # Вычисляем количество звездочек
    print(" " * spaces + "*" * stars)  # Выводим строку

# Вариант 3: Елочка с ярусами и фиксированной высотой яруса (по умолчанию 3)
# Запрашиваем у пользователя количество ярусов и высоту яруса
tiers = int(input("Сколько ярусов у ёлочки? "))  # Количество ярусов
height_per_tier = 3  # Высота каждого яруса (по умолчанию 3, можно изменить)

# Строим ёлочку
for tier in range(tiers):  # Перебираем ярусы
    for row in range(height_per_tier):  # Перебираем строки в ярусе
        num_spaces = tiers + height_per_tier - tier - row - 1  # Вычисляем количество пробелов
        num_stars = 2 * (tier + row) + 1  # Вычисляем количество звездочек
        print(" " * num_spaces + "*" * num_stars)  # Выводим строку
