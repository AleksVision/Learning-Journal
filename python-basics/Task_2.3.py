""" Напишите програму которая вычесляет площадь круга и длину окружности
по введенному диаметру.
Диаметр не превышает 1000 у.е.
Точность вычисления должна составлять не менее 42 знаков после запятой.
"""
"""Итог:
Решение 2: Обеспечивает более высокую точность за счёт использования фиксированного
точного значения π в виде строки. Это значение более стабильно и предсказуемо.

Решение 1: Использует значение π из модуля math, что может приводить к небольшим
отклонениям из-за округления.

Если вам нужно наивысшая точность, рекомендуется использовать Решение 2 с фиксированным значением π.
Однако оба подхода корректны и дают достаточно точные результаты для большинства практических задач.
"""

from decimal import Decimal, getcontext
import math

# Устанавливаем точность вычислений до 50 знаков после запятой
getcontext().prec = 50

# Решение 1: Использование Decimal и значения π из модуля math
print("Решение 1\n")

# Ввод диаметра круга
diameter = Decimal(input("Введите диаметр круга (не более 1000 у.е.): \n"))

# Проверка на допустимый диапазон
if diameter > 1000:
    print("Диаметр не должен превышать 1000 у.е.")
else:
    # Вычисление радиуса
    radius = diameter / 2
    
    # Использование константы π с высокой точностью
    pi = Decimal(math.pi)
    
    # Вычисление площади круга
    area = pi * radius**2
    
    # Вычисление длины окружности
    circumference = 2 * pi * radius
    
    # Вывод результатов с высокой точностью
    print(f"Площадь круга: {area}")
    print(f"Длина окружности: {circumference}")
    print(f"Точность вычисления длины окружности: {len(str(circumference))} знаков после запятой")

# Решение 2: Использование точного значения π в виде строки
