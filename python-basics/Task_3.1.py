"""
Задача 2
Пользователь вводит данные. Сделайте проверку данных и преобразуйте если возможно в один из вариантов ниже:
Целое положительное число
Вещественное положительное или отрицательное число
Строку в нижнем резистре, если в строке есть хотя бы одна заглавная буква
Строку в верхнем регистре в остальных случаям 
"""
# Решение 1

# Запрашиваем данные у пользователя
data = input("Введите данные: ")

if data.isdecimal():  # Проверяем, что введено целое положительное число
    result = int(data)  # Преобразуем строку в целое число
    print(f"Целое положительное число: {result}")
else:
    try:
        result = float(data)  # Проверяем, что введено вещественное положительное или отрицательное число
        print(f"Вещественное число: {result}")
    except ValueError:
        if data.islower() != data:  # Проверяем, есть ли в строке хотя бы одна заглавная буква
            print(f"Строка в нижнем регистре: {data.lower()}")
        else:
            print(f"Строка в верхнем регистре: {data.upper()}")



# Решение 2

def transform_input(user_input):
    try:
        # Попытка преобразовать ввод в целое положительное число
        number = int(user_input)
        if number > 0:
            return f"Целое положительное число: {number}"
        else:
            return "Введенное число не является положительным целым числом."
    except ValueError:
        try:
            # Попытка преобразовать ввод в вещественное число
            float_number = float(user_input)
            return f"Вещественное число: {float_number}"
        except ValueError:
            # Проверка на наличие заглавных букв в строке
            if any(char.isupper() for char in user_input):
                return f"Строка в нижнем регистре: {user_input.lower()}"
            else:
                return f"Строка в верхнем регистре: {user_input.upper()}"

# Пример использования
user_input = input("Введите данные: ")
result = transform_input(user_input)
print(result)
