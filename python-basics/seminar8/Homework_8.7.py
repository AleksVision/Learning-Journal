import os
from typing import List, Dict, Any
from file_utils.file_formats import save_to_json, save_to_csv, save_to_pickle

def get_directory_size(path: str) -> int:
    """–í—ã—á–∏—Å–ª—è–µ—Ç –æ–±—â–∏–π —Ä–∞–∑–º–µ—Ä —Ñ–∞–π–ª–æ–≤ –≤ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏."""
    total_size = 0
    for dirpath, _, filenames in os.walk(path):
        for filename in filenames:
            filepath = os.path.join(dirpath, filename)
            try:
                total_size += os.path.getsize(filepath)
            except OSError as e:
                print(f"–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ª—É—á–µ–Ω–∏–∏ —Ä–∞–∑–º–µ—Ä–∞ {filepath}: {e}")
    return total_size

def process_directory(directory: str, 
                     json_filename: str = 'files_info.json',
                     csv_filename: str = 'files_info.csv',
                     pickle_filename: str = 'files_info.pkl') -> bool:
    """–û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –≤ —Ä–∞–∑–Ω—ã—Ö —Ñ–æ—Ä–º–∞—Ç–∞—Ö."""
    try:
        if not os.path.exists(directory):
            raise FileNotFoundError(f"–î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è {directory} –Ω–µ –Ω–∞–π–¥–µ–Ω–∞")
            
        file_info = []
        for dirpath, dirnames, filenames in os.walk(directory):
            # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Ñ–∞–π–ª–æ–≤
            for name in filenames:
                path = os.path.join(dirpath, name)
                file_info.append({
                    'name': name,
                    'path': path,
                    'parent': os.path.basename(dirpath),
                    'type': 'file',
                    'size': os.path.getsize(path)
                })
            
            # –û–±—Ä–∞–±–æ—Ç–∫–∞ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–π
            for name in dirnames:
                path = os.path.join(dirpath, name)
                file_info.append({
                    'name': name,
                    'path': path,
                    'parent': os.path.basename(dirpath),
                    'type': 'directory',
                    'size': get_directory_size(path)
                })
        
        # –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –≤ —Ä–∞–∑–Ω—ã—Ö —Ñ–æ—Ä–º–∞—Ç–∞—Ö
        save_to_json(file_info, json_filename)
        save_to_csv(file_info, csv_filename)
        save_to_pickle(file_info, pickle_filename)
        
        print(f"‚úÖ –î–∞–Ω–Ω—ã–µ —Å–æ—Ö—Ä–∞–Ω–µ–Ω—ã –≤ —Ñ–∞–π–ª—ã:")
        print(f"üìÑ JSON: {json_filename}")
        print(f"üìä CSV: {csv_filename}")
        print(f"üóÉÔ∏è Pickle: {pickle_filename}")
        
        return True
        
    except Exception as e:
        print(f"‚ùå –û—à–∏–±–∫–∞ –ø—Ä–∏ –æ–±—Ä–∞–±–æ—Ç–∫–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏: {e}")
        return False
